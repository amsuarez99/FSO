const typeDefs = gql`
  type Book {
    title: String!
    published: Int!
    author: String!
    id: ID!
    genres: [String!]!
  }

  type Author {
    name: String!
    id: ID!
    born: Int
    bookCount: Int!
  }

  type Query {
    bookCount: Int!
    authorCount: Int!
    allAuthors: [Author!]!
    allBooks(author: String, genre: String): [Book!]!
  }

  type Mutation {
    addBook(
      title: String!
      author: String!
      published: Int!
      genres: [String!]!
    ): Book
    editAuthor(name: String!, setBornTo: Int!): Author
  }
`

const resolvers = {
  Query: {
    bookCount: () => books.length,
    authorCount: () => authors.length,
    allAuthors: () => authors,
    allBooks: (_, args) => {
      let filteredBooks = books

      if (args.author) {
        filteredBooks = filteredBooks.filter(
          (book) => book.author === args.author
        )
      }

      if (args.genre) {
        filteredBooks = filteredBooks.filter((book) =>
          book.genres.includes(args.genre)
        )
      }

      return filteredBooks
    },
  },
  Author: {
    bookCount: (root) =>
      books?.filter((book) => book.author === root.name).length ?? 0,
  },
  Mutation: {
    addBook: (_, args) => {
      const newBook = { ...args, id: uuid() }
      const bookAuthor = authors.find((author) => author.name === args.author)
      if (!bookAuthor) {
        authors = authors.concat({ name: args.author, id: uuid() })
      }

      books = books.concat(newBook)
      return newBook
    },
    editAuthor: (_, args) => {
      const foundAuthor = authors.find((author) => author.name === args.name)
      if (!foundAuthor) {
        return null
      }

      const newAuthor = { ...foundAuthor, born: args.setBornTo }

      authors = authors.map((author) => {
        return author.name === args.name ? newAuthor : author
      })

      return newAuthor
    },
  },
}

const server = new ApolloServer({
  typeDefs,
  resolvers,
})

server.listen().then(({ url }) => {
  console.log(`Server ready at ${url}`)
})
